function [h0,h1,f0,f1]=binary(mi,ni)

m=min([mi ni]);
n=max([mi ni]);

if m==3 & n==5 
  h0=[1 2 1]/4;
  f0=[-1 2 6 2 -1]/4;
elseif m==7 & n==9
  h0=[-1 0 9 16 9 0 -1]/16;
  f0=[1 0 -8 16 46 16 -8 0 1]/64;
elseif m==7 & n==13
  h0=[-1 0 9 16 9 0 -1]/16;
  f0=[-1 0 18 -16 -63 144 348 144 -63 -16 18 0 -1]/512;
elseif m==5 & n==11
  h0=[-1 2 6 2 -1]/4;
  f0=[1 2 -7 0 70 124 70 0 -7 2 1]/256;
elseif m==11 & n==13
  h0=[3 0 -25 0 150 256 150 0 -25 0 3]/256;
  f0=[-3 0 22 0 -125 256 724 256 -125 0 22 0 -3]/1024;
elseif m==2 & n==6
  h0=[1 1]/2;
  f0=[-1 1 8 8 1 -1]/8;
elseif m==6 & n==10
  h0=[-1 1 8 8 1 -1]/8;
  f0=[1 1 -8 8 62 62 8 -8 1 1]/128;
else
  error('No %d/%d binary filter pair found.',mi,ni)
end

if mi>m
  z=h0;
  h0=f0;
  f0=z;
end

p0=length(h0);
p1=length(f0);

if rem(p0,2)>0
  h1=(-1).^[-(p1-1)/2:(p1-1)/2].*f0;
  f1=(-1).^[-(p0-1)/2:(p0-1)/2].*h0;
else
  h1=(-1).^[0:p1-1].*f0;
  f1=(-1).^[0:p0-1].*h0;
end
